def count_sheep(n):
    return ''.join(f"{i} sheep..." for i in range(1, n+1))

print(count_sheep(0))  # Output: ""
print(count_sheep(1))  # Output: "1 sheep..."
print(count_sheep(2))  # Output: "1 sheep...2 sheep..."
print(count_sheep(3))  # Output: "1 sheep...2 sheep...3 sheep..."
print(count_sheep(4))  # Output: "1 sheep...2 sheep...3 sheep...4 sheep...
print(count_sheep(5))  # Output: "1 sheep...2 sheep...3 sheep...4 sheep...5 sheep..."
